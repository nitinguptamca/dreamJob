package billionaire.nitin.kumar.gupta.CtCI6thEdition1.Ch13Java.Q13_06_Object_Reflection;

public class Rectangle {
    private double width;
    private double height;

    public Rectangle(double w, double h) {
        width = w;
        height = h;
    }

    public double area() {
        return width * height;
    }
}
